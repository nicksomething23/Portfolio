const freelance = document.querySelector('#freelance')
const career = document.querySelector('#career')


document.querySelector("#freelanceButton").addEventListener("click",showFreelance)
document.querySelector("#careerButton").addEventListener("click",showCareer)


function showFreelance (){
	career.classList.add('hiddenCareer')
	freelance.classList.toggle('hiddenFreelance')
}

function showCareer (){
	freelance.classList.add('hiddenFreelance')
	career.classList.toggle('hiddenCareer')
}

if (history.scrollRestoration) {
    history.scrollRestoration = 'manual';
} else {
    window.onbeforeunload = function () {
        window.scrollTo(0, 0);
    }
}